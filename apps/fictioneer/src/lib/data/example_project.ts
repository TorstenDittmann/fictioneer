// Helper function to count words in HTML content
function count_words(html: string): number {
	const text = html.replace(/<[^>]*>/g, '');
	return text
		.trim()
		.split(/\s+/)
		.filter((word) => word.length > 0).length;
}

// Helper function to count characters in HTML content
function count_characters(html: string): number {
	const text = html.replace(/<[^>]*>/g, '');
	return text.length;
}

